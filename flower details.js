// ========================
// BACK BUTTON
// ========================

function goBack() {

    document.getElementById("detailPage")
        .classList.remove("active");

    document.getElementById("homePage")
        .classList.add("active");
}



// ========================
// NEW IMPROVED WORD DOWNLOAD
// ========================

function downloadWord() {

    const painting = JSON.parse(
        localStorage.getItem("painting")
    );

    // Agar data na mile to error se bachne ke liye
    if (!painting) {
        alert("Painting data not found!");
        return;
    }

    // HTML format text jo MS Word ke liye sabse best compatible hai
    let htmlContent = `
    <html xmlns:o='urn:schemas-microsoft-com:office:office' xmlns:w='urn:schemas-microsoft-com:office:word' xmlns='http://www.w3.org/TR/REC-html40'>
    <head><title>${painting.title}</title>
    <style>
        body { font-family: Arial, sans-serif; line-height: 1.6; }
        h1 { color: #333333; border-bottom: 2px solid #333; padding-bottom: 5px; }
        p { margin: 10px 0; font-size: 12pt; }
        b { color: #000000; }
    </style>
    </head>
    <body>
        <h1>Title: ${painting.title}</h1>
        <p><b>Description:</b> ${painting.description}</p>
        <p><b>Price:</b> ${painting.price}</p>
        <p><b>Artist:</b> ${painting.artist}</p>
        <p><b>Artist Quote:</b> ${painting.ArtistQuote}</p>
        <p><b>Materials:</b> ${painting.Materials}</p>
        <p><b>Size:</b> ${painting.size}</p>
        <p><b>Category:</b> ${painting.category}</p>
        <p><b>Year:</b> ${painting.year}</p>
    </body>
    </html>
    `;

    // Standard application/msword format data create karna
    let blob = new Blob(['\ufeff' + htmlContent], {
        type: 'application/msword'
    });

    // Browser download link simulation
    let link = document.createElement("a");
    link.href = URL.createObjectURL(blob);
    link.download = painting.title.replace(/[^a-zA-Z0-9]/g, "_") + ".doc"; // Special characters hatane ke liye

    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link); // Clean up memory
}