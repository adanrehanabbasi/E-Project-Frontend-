// ========================
// OPEN DETAIL PAGE
// ========================

function openDetail(index) {

    localStorage.setItem(
        "painting",
        JSON.stringify(paintings[index])
    );

    // Hide Home Page
    document.getElementById("homePage")
        .classList.remove("active");

    // Show Detail Page
    document.getElementById("detailPage")
        .classList.add("active");

    showDetail();
}



